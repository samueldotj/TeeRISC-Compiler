#ifndef TeeRISC_SUBTARGET_H
#define TeeRISC_SUBTARGET_H

#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "TeeRISCGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class TeeRISCSubtarget : public TeeRISCGenSubtargetInfo {
  virtual void anchor();
  bool IsV2;
  
public:
  TeeRISCSubtarget(const std::string &TT, const std::string &CPU,
                 const std::string &FS, bool is64bit=false);

  bool isV2() const { return IsV2; }
  
  /// ParseSubtargetFeatures - Parses features string setting specified 
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
  
  // DataLayout syntax is defined by LLVM Language Reference
  // http://llvm.org/docs/LangRef.html#id573
  std::string getDataLayout() const {
    const char *p;
    p = "e-p:32:32:32-i32:32:32";
    return std::string(p);
  }
};

} // end namespace llvm

#endif
